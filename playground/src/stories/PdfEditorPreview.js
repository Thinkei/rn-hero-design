import React from 'react';
import { flow, map, flatten, find, first, get, toString } from 'lodash/fp';
import { PdfEditorPreview } from 'rn-hero-design';

const PdfEditorPreviewScreen = () => {
  const file = React.useMemo(
    () => getFirstAttachmentPdfFile(sampleResponse.data.contract_attachments),
    [],
  );

  const variableValues = React.useMemo(
    () => getContractTemplateVariableValues(sampleResponse.data.variables),
    [],
  );

  return <PdfEditorPreview file={file} variableValues={variableValues} />;
};

const getFirstAttachmentPdfFile = flow(first, get('remote_url'));

const findSelectedValue = (value, options) => {
  if (value === null) {
    return value;
  }

  return find({ value: toString(value) })(options) ? value : null;
};

const getInputValue = ({ type, value, example, blockOptions, isEditMode }) => {
  if (type === 'Select') {
    const options = blockOptions.map(({ id, option }) => ({
      value: toString(id),
      displayText: option,
    }));
    const exampleValue = options[0].value;
    return {
      variable_type: type,
      options,
      value: isEditMode ? exampleValue : findSelectedValue(value, options),
      example: exampleValue,
    };
  }

  return {
    variable_type: type,
    options: [],
    value: isEditMode ? example : value,
    example,
  };
};

const mapVariableValue = (isEditMode) => ({
  variable_name: variableName,
  id: variableId,
  type,
  block_options: blockOptions,
  extra_values: extraValues,
  example_data: exampleData,
  placeholder,
  value,
}) =>
  map(({ id, data }) => {
    const { location, formatting, area } = data || {};

    return {
      id: toString(id),
      variable_id: toString(variableId),
      variable_name: variableName,
      location,
      formatting,
      area,
      input_value: getInputValue({
        type,
        example: exampleData,
        value,
        blockOptions,
        isEditMode,
      }),
      placeholder,
    };
  })(extraValues);

const getContractTemplateVariableValues = flow(
  map(mapVariableValue(false)),
  flatten,
);

